package collection;

import java.util.LinkedHashSet;

public class LinkedHashSetMethods {

	public static void main(String[] args) {
	
		
		LinkedHashSet ls = new LinkedHashSet();
		
		ls.add(100);
		ls.add(200);
		ls.add(300);
		ls.add(400);
		ls.add(500);
		System.out.println(ls);// insertion order is preserved in linkedHashSet
		

	}

}
