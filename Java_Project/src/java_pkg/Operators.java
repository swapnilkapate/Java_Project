package java_pkg;

public class Operators {

	public static void main(String[] args) {
		
		int i= 4;
		System.out.println(i); // 4
		
		i++;
		System.out.println(i); // 5
		
		++i;
		System.out.println(i); // 6
		
		System.out.println(i++); // 6 as it print value of i first and then it increments
		System.out.println(++i); // 8 as first increments then prints
		System.out.println(i); // 8
		
		
		
		
		

	}

}
